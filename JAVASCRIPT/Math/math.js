/** METODOS DE MATH */

let  numero

numero = Math.sqrt(25); // La función Math.sqrt() devuelve la raíz cuadrada de un número.
console.log(numero);

numero = Math.cbrt(27); // La función Math.cbrt() devuelve la raíz cúbica de un número.
console.log(numero);

numero = Math.max(5, 10, 15, 0, 30, 1, 100); // La función Math.max() devuelve el número más grande de un conjunto de números.
console.log(numero);

numero = Math.min(5, 10, 15, 0, 30, 1, 100); // La función Math.min() devuelve el número más pequeño de un conjunto de números.
console.log(numero);

numero = Math.random(); // La función Math.random() devuelve un número aleatorio entre 0 (inclusive) y 1 (exclusivo).
console.log(numero);

numero = Math.random()*100; // Multiplicamos el número aleatorio por 100 para obtener un número entre 0 y 100.
console.log(numero);

numero = Math.round(4.7); // La función Math.round() devuelve el valor de un número redondeado al entero más cercano.
console.log(numero);

numero = Math.floor(4.7); // La función Math.floor() devuelve el mayor entero menor o igual a un número dado.
console.log(numero);

numero = Math.fround(4.7549999999999); // La función Math.fround() devuelve la representación de precisión simple de un número.
console.log(numero);

numero = Math.trunc(5.99999); // La función Math.trunc() devuelve la parte entera de un número eliminando los dígitos decimales.
console.log(numero);

